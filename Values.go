package main
import "fmt"
func main() {
fmt.Println("se" + "oul")
fmt.Println("1 + 1 =", 1+1)
fmt.Println("9 * 9 =", 9*9)
fmt.Println("7.0/3.0 =", 7.0/3.0)
fmt.Println(true && false)
fmt.Println(true || false)
fmt.Println(!true)
fmt.Println(nil)
}

